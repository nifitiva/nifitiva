# --- DATABASE REST HELPERS ---
function Read-JSON($filePath) {
    try {
        if (-not (Test-Path $filePath)) { return @() }
        $content = [System.IO.File]::ReadAllText($filePath)
        if ([string]::IsNullOrWhiteSpace($content)) { return @() }
        $parsed = ConvertFrom-Json $content
        if ($null -eq $parsed) { return @() }
        return @($parsed)
    } catch {
        return @()
    }
}

function Write-JSON($filePath, $data) {
    try {
        $json = ConvertTo-Json $data -Depth 100
        [System.IO.File]::WriteAllText($filePath, $json)
        return $true
    } catch {
        return $false
    }
}

# Rebuild static products.js database for GitHub Pages storefront
