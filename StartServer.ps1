"@
        [System.IO.File]::WriteAllText($rootProductsJs, $fileContent)
        return $true
    } catch {
        return $false
    }
}

# Inject Affiliate tag parameters
function Inject-AffiliateTag($url, $tag) {
    if ([string]::IsNullOrEmpty($url)) { return "" }
    if ([string]::IsNullOrEmpty($tag)) { return $url }
    
    if ($url.Contains("amazon.in") -or $url.Contains("amazon.com") -or $url.Contains("amzn.to")) {
        if ($url.Contains("?")) {
            if ($url.Contains("tag=")) {
                # Replace existing tag
                return $url -replace "tag=[^&]*", "tag=$tag"
            } else {
                return "$url&tag=$tag"
            }
        } else {
            return "$url?tag=$tag"
        }
    }
    return $url
}

