# Start background TCP proxy to rewrite Host headers (supports external tunnels/mobile)
$proxyScript = {
    param($lPort, $tPort)
    $proxyListener = [System.Net.Sockets.TcpListener]::new([System.Net.IPAddress]::Any, $lPort)
    $proxyListener.Start()
    while ($true) {
        try {
            $c = $proxyListener.AcceptTcpClient()
            $stream = $c.GetStream()
            $buffer = New-Object byte[] 65535
            $read = $stream.Read($buffer, 0, $buffer.Length)
            if ($read -gt 0) {
                $req = [System.Text.Encoding]::UTF8.GetString($buffer, 0, $read)
                $req = $req -replace "Host: .*`r`n", ("Host: localhost:" + $tPort + "`r`n")
                $target = New-Object System.Net.Sockets.TcpClient("127.0.0.1", $tPort)
                $tStream = $target.GetStream()
                $reqBytes = [System.Text.Encoding]::UTF8.GetBytes($req)
                $tStream.Write($reqBytes, 0, $reqBytes.Length)
                $tBuffer = New-Object byte[] 65535
                $tRead = $tStream.Read($tBuffer, 0, $tBuffer.Length)
                while ($tRead -gt 0) {
                    $stream.Write($tBuffer, 0, $tRead)
                    if ($tStream.DataAvailable) {
                        $tRead = $tStream.Read($tBuffer, 0, $tBuffer.Length)
                    } else {
                        break
                    }
                }
                $target.Close()
            }
            $c.Close()
        } catch {
            if ($null -ne $c) { $c.Close() }
        }
    }
}
