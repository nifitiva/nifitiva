app.post('/api/pinterest/schedule', (req, res) => {
  const schedule = readJSON(SCHEDULE_FILE);
  const { title, description, videoUrl, imageUrl, affiliateUrl, boardName, scheduleTime } = req.body;

  if (!title || (!videoUrl && !imageUrl) || !scheduleTime) {
    return res.status(400).json({ error: 'Title, media content, and Schedule Time are required.' });
  }

  const newScheduleItem = {
    id: 'pin_' + Date.now() + '_' + Math.random().toString(36).substr(2, 5),
    title: title.trim(),
    description: description || '',
    videoUrl: videoUrl || '',
    imageUrl: imageUrl || '',
    affiliateUrl: affiliateUrl || '',
    boardName: boardName || 'Viral Amazon Finds',
    scheduleTime: scheduleTime, // Format: YYYY-MM-DDTHH:MM
    status: 'Pending', // Pending, Posted, Failed
    pinUrl: '',
    postedTime: '',
    views: 0,
    clicks: 0
  };

  schedule.push(newScheduleItem);
  
  // Sort schedule queue by date chronological
  schedule.sort((a, b) => new Date(a.scheduleTime) - new Date(b.scheduleTime));

  if (writeJSON(SCHEDULE_FILE, schedule)) {
    res.status(201).json(newScheduleItem);
  } else {
    res.status(500).json({ error: 'Failed to schedule pin.' });
  }
});

