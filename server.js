// --- BACKGROUND AUTOMATION SCHEDULE RUNNER ---
// Checks every 15 seconds for scheduled pins that need to be published
setInterval(async () => {
  const schedule = readJSON(SCHEDULE_FILE);
  const config = readJSON(CONFIG_FILE, defaultConfigs);
  const now = new Date();
  
  let modified = false;

  for (let i = 0; i < schedule.length; i++) {
    const item = schedule[i];
    if (item.status === 'Pending' && new Date(item.scheduleTime) <= now) {
      console.log(`[AUTOMATOR] Processing Scheduled Pin upload: "${item.title}"`);
      
      if (config.simulationMode) {
        // SIMULATED UPLOAD PROCESS
        item.status = 'Posted';
        item.postedTime = new Date().toISOString();
        item.pinUrl = `https://pinterest.com/pin/simulated_${Math.random().toString(36).substr(2, 9)}`;
        
        // Generate random organic metrics for simulated pins
        item.views = Math.floor(Math.random() * 250) + 50;
        item.clicks = Math.floor(item.views * (Math.random() * 0.15 + 0.02)); // 2-17% click-through rate

        // Auto-increment simulated views/clicks on the matching product too
        const products = readJSON(PRODUCTS_FILE);
        const matchIdx = products.findIndex(p => p.title.toLowerCase() === item.title.toLowerCase() || p.affiliateUrl === item.affiliateUrl);
        if (matchIdx !== -1) {
          products[matchIdx].views = (products[matchIdx].views || 0) + item.views;
          products[matchIdx].clicks = (products[matchIdx].clicks || 0) + item.clicks;
          writeJSON(PRODUCTS_FILE, products);
          rebuildStaticStorefrontProducts();
        }

        console.log(`[AUTOMATOR] [SIMULATION] Pin successfully posted to board "${item.boardName}". Direct Pin Link: ${item.pinUrl}`);
        modified = true;
      } else {
        // REAL PINTEREST API CREATION WORKFLOW
        try {
          if (!config.pinterestAccessToken || !config.pinterestBoardId) {
            console.error('[AUTOMATOR] [ERROR] Real Posting selected, but Access Token or Board ID is missing! Pin marked as Failed.');
            item.status = 'Failed';
            item.postedTime = new Date().toISOString();
            modified = true;
            continue;
          }

          // Create Pin Body for Pinterest API v5
          const pinBody = {
            title: item.title,
            description: item.description,
            link: item.affiliateUrl,
            board_id: config.pinterestBoardId,
            media_source: {
              source_type: 'image_url',
              url: item.videoUrl || item.imageUrl // Pinterest API v5 requires a media source url
            }
          };

          const pinResponse = await axios.post('https://api.pinterest.com/v5/pins', pinBody, {
            headers: {
              'Authorization': `Bearer ${config.pinterestAccessToken}`,
              'Content-Type': 'application/json'
            }
          });

          if (pinResponse.data && pinResponse.data.id) {
            item.status = 'Posted';
            item.postedTime = new Date().toISOString();
            item.pinUrl = `https://www.pinterest.com/pin/${pinResponse.data.id}/`;
            console.log(`[AUTOMATOR] [SUCCESS] Real Pinterest Pin Created! ID: ${pinResponse.data.id}`);
          } else {
            throw new Error('API returned unexpected response formatting.');
          }

        } catch (apiError) {
          console.error('[AUTOMATOR] [API ERROR] Failed to create Pin on Pinterest:', apiError.response ? apiError.response.data : apiError.message);
          item.status = 'Failed';
          item.postedTime = new Date().toISOString();
        }
        modified = true;
      }
    }
  }

  if (modified) {
    writeJSON(SCHEDULE_FILE, schedule);
  }
}, 15000);

