app.post('/api/products', (req, res) => {
  const products = readJSON(PRODUCTS_FILE);
  const config = readJSON(CONFIG_FILE, defaultConfigs);
  const productData = req.body;

  if (!productData.title || !productData.price) {
    return res.status(400).json({ error: 'Title and Price are required.' });
  }

  // Inject affiliate tag into product link
  const affiliateUrl = injectAffiliateTag(productData.affiliateUrl || 'https://www.amazon.in', config.amazonTag);

  const newProduct = {
    id: 'prod_' + Date.now() + '_' + Math.random().toString(36).substr(2, 5),
    title: productData.title,
    description: productData.description || '',
    category: productData.category || 'Viral Finds',
    videoUrl: productData.videoUrl || '',
    imageUrl: productData.imageUrl || 'https://images.unsplash.com/photo-1523275335684-37898b6baf30?w=600',
    price: Number(productData.price),
    originalPrice: Number(productData.originalPrice || productData.price * 2),
    affiliateUrl: affiliateUrl,
    rating: Number(productData.rating || 4.5),
    views: 0,
    clicks: 0,
    tags: productData.tags || []
  };

  products.push(newProduct);
  if (writeJSON(PRODUCTS_FILE, products)) {
    rebuildStaticStorefrontProducts();
    res.status(201).json(newProduct);
  } else {
    res.status(500).json({ error: 'Failed to save product.' });
  }
});

app.put('/api/products/:id', (req, res) => {
  const products = readJSON(PRODUCTS_FILE);
  const config = readJSON(CONFIG_FILE, defaultConfigs);
  const { id } = req.params;
  const index = products.findIndex(p => p.id === id);

  if (index === -1) {
    return res.status(404).json({ error: 'Product not found.' });
  }

  const existing = products[index];
  const updateData = req.body;

  // Sync details
  if (updateData.title !== undefined) existing.title = updateData.title;
  if (updateData.description !== undefined) existing.description = updateData.description;
  if (updateData.category !== undefined) existing.category = updateData.category;
  if (updateData.videoUrl !== undefined) existing.videoUrl = updateData.videoUrl;
  if (updateData.imageUrl !== undefined) existing.imageUrl = updateData.imageUrl;
  if (updateData.price !== undefined) existing.price = Number(updateData.price);
  if (updateData.originalPrice !== undefined) existing.originalPrice = Number(updateData.originalPrice);
  if (updateData.rating !== undefined) existing.rating = Number(updateData.rating);
  if (updateData.tags !== undefined) existing.tags = updateData.tags;
  
  if (updateData.affiliateUrl !== undefined) {
    existing.affiliateUrl = injectAffiliateTag(updateData.affiliateUrl, config.amazonTag);
  }

  products[index] = existing;
  if (writeJSON(PRODUCTS_FILE, products)) {
    rebuildStaticStorefrontProducts();
    res.json(existing);
  } else {
    res.status(500).json({ error: 'Failed to update product.' });
  }
});

