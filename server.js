// 5. GITHUB PAGES ONE-CLICK COMMIT & PUSH
app.post('/api/github/push', (req, res) => {
  console.log('[GITHUB] Initiating 1-Click Sync to GitHub Pages...');
  const products = readJSON(PRODUCTS_FILE);

  // Execute Git Workflow
  exec('git add .', (err1, stdout1, stderr1) => {
    if (err1) {
      console.error('[GITHUB] git add failed:', stderr1);
      return res.status(500).json({ error: 'Git Add failed.', details: stderr1 });
    }

    const commitMsg = `Sync storefront: updated dynamic catalog to ${products.length} products.`;
    exec(`git commit -m "${commitMsg}"`, (err2, stdout2, stderr2) => {
      // Note: Git returns error if nothing to commit, check if it contains "nothing to commit" or similar
      if (err2 && !stdout2.includes('nothing to commit') && !stderr2.includes('nothing to commit')) {
        console.error('[GITHUB] git commit failed:', stderr2);
        return res.status(500).json({ error: 'Git Commit failed.', details: stderr2 });
      }

      exec('git push', (err3, stdout3, stderr3) => {
        if (err3) {
          console.error('[GITHUB] git push failed:', stderr3);
          return res.status(500).json({ error: 'Git Push failed. Make sure upstream credentials are set up.', details: stderr3 });
        }

        console.log('[GITHUB] Deployment successful!');
        res.json({
          success: true,
          message: 'Successfully deployed to GitHub Pages! Your site will reflect changes in a few moments.',
          consoleLog: [stdout1, stdout2, stdout3].join('\n')
        });
      });
    });
  });
});

