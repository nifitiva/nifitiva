// 3. PINTEREST SCRAPER & VIDEO DOWNLOADER
app.post('/api/pinterest/fetch', async (req, res) => {
  const { url } = req.body;
  if (!url) {
    return res.status(400).json({ error: 'Pinterest URL is required.' });
  }

  try {
    let targetUrl = url;
    // Resolve short.it redirect links
    if (url.includes('pin.it')) {
      const redirectRes = await axios.get(url, {
        maxRedirects: 5,
        headers: {
          'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/120.0.0.0 Safari/537.36'
        }
      });
      targetUrl = redirectRes.request.res.responseUrl || url;
    }

    const response = await axios.get(targetUrl, {
      headers: {
        'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/120.0.0.0 Safari/537.36',
        'Accept-Language': 'en-US,en;q=0.9'
      }
    });

    const html = response.data;
    const $ = cheerio.load(html);

    let title = $('meta[property="og:title"]').attr('content') || $('title').text() || 'Pinterest Pin';
    let description = $('meta[property="og:description"]').attr('content') || $('meta[name="description"]').attr('content') || '';
    let imageUrl = $('meta[property="og:image"]').attr('content') || '';
    let videoUrl = $('meta[property="og:video"]').attr('content') || $('meta[property="og:video:secure_url"]').attr('content') || '';

    title = title.replace(' | Pinterest', '').trim();

    // Check application/ld+json standard schema script block
    $('script[type="application/ld+json"]').each((i, elem) => {
      try {
        const json = JSON.parse($(elem).html());
        if (json) {
          const items = Array.isArray(json) ? json : [json];
          for (const item of items) {
            if (item['@type'] === 'VideoObject' || item.video) {
              const videoObj = item.video || item;
              if (videoObj.contentUrl) videoUrl = videoObj.contentUrl;
              if (videoObj.thumbnailUrl) imageUrl = videoObj.thumbnailUrl;
              if (item.name) title = item.name;
              if (item.description) description = item.description;
            } else if (item['@type'] === 'SocialMediaPosting' || item['@type'] === 'CreativeWork') {
              if (item.name) title = item.name;
              if (item.description) description = item.description;
              if (item.image) {
                imageUrl = Array.isArray(item.image) ? item.image[0] : (item.image.url || item.image);
              }
            }
          }
        }
      } catch (e) {}
    });

    // Cleanup Pinterest marketing descriptions
    description = description.replace(/Discover recipes, home ideas, style inspiration.*/i, '').trim();

    // Scan raw html using regex as last resort
    if (!videoUrl) {
      const mp4Regex = /"videoUrl"\s*:\s*"([^"]+)"/g;
      let match = mp4Regex.exec(html);
      if (match && match[1]) {
        videoUrl = match[1].replace(/\\u002F/g, '/');
      }
    }

    if (!videoUrl) {
      const pinimgVideoRegex = /https:\/\/v1\.pinimg\.com\/videos\/[^\s"']+\.mp4/g;
      let match = html.match(pinimgVideoRegex);
      if (match && match[0]) {
        videoUrl = match[0];
      }
    }

    // Extract tags
    const tags = [];
    const hashtagRegex = /#\w+/g;
    const matches = description.match(hashtagRegex);
    if (matches) {
      matches.forEach(tag => {
        if (!tags.includes(tag.toLowerCase())) {
          tags.push(tag.toLowerCase());
        }
      });
    }

    res.json({
      success: true,
      title: title.trim(),
      description: description.trim(),
      videoUrl: videoUrl || null,
      imageUrl: imageUrl || null,
      tags
    });

  } catch (error) {
    console.error('[SCRAPER] Pinterest fetch failed:', error.message);
    res.status(500).json({ error: 'Failed to scrape Pinterest pin. Make sure it is a valid public pin link.' });
  }
});

