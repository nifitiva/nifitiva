// --- DATABASE HELPERS ---
function readJSON(file, fallback = []) {
  try {
    if (!fs.existsSync(file)) return fallback;
    const data = fs.readFileSync(file, 'utf8');
    return JSON.parse(data);
  } catch (error) {
    console.error(`Error reading file ${file}:`, error);
    return fallback;
  }
}

function writeJSON(file, data) {
  try {
    fs.writeFileSync(file, JSON.stringify(data, null, 2));
    return true;
  } catch (error) {
    console.error(`Error writing file ${file}:`, error);
    return false;
  }
}

// Regenerate the static products.js inside the root folder
function rebuildStaticStorefrontProducts() {
  try {
    const products = readJSON(PRODUCTS_FILE);
    const fileContent = `// ==========================================================================
// NIFITIVA AUTOMATED PRODUCTS DATABASE - GENERATED BY THE ADMIN DASHBOARD
// DO NOT EDIT THIS FILE MANUALLY. IT WILL BE OVERWRITTEN UPON SYNC.
// ==========================================================================

const PRODUCTS_DATABASE = ${JSON.stringify(products, null, 2)};
`;
    fs.writeFileSync(ROOT_PRODUCTS_JS, fileContent, 'utf8');
    console.log(`[SYNC] Regenerated root products.js with ${products.length} items successfully.`);
    return true;
  } catch (error) {
    console.error('[SYNC] Error rebuilding products.js:', error);
    return false;
  }
}

// Inject Affiliate parameters into URLs (specifically Amazon India)
function injectAffiliateTag(url, tag) {
  if (!url) return '';
  if (!tag) return url;
  
  try {
    // Check if it's an Amazon link
    if (url.includes('amazon.in') || url.includes('amazon.com') || url.includes('amzn.to')) {
      const parsedUrl = new URL(url);
      parsedUrl.searchParams.set('tag', tag);
      return parsedUrl.toString();
    }
  } catch (e) {
    // Return original url if parsing fails
  }
  return url;
}

